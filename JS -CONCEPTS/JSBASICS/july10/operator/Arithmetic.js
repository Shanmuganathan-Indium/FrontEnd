// Arithmetic - To perform arithmetic operations

let s = 33;
let h = 48;

console.log(s + h);
console.log(s - h);
console.log(s * h);
console.log(s / h);
console.log(s % h);

console.log(" Post Increment ");

// Post increment value doesn't change one line
// Once line completed value will be incremented
console.log(s++);

console.log("pre Increment");

// During this line execute apply change in this line
console.log(++s);

console.log("Post Decrement");
console.log(h--)
console.log(h)



console.log("pre Decrement");
console.log(--h)
console.log(h)



